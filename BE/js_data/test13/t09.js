//논리 연산자
// AND 논리 연산자 &&
console.log(true && true);
console.log(true && false);
console.log('문장' == '문장' && 5 ==5 );
console.log(5 == 5 && '다른문장1' == '다른문장2');
console.log('-----------------------------');
// OR 논리 연산자 ||
console.log(true || false);
console.log(false || false);
console.log('문장' == '문장' || 5 == 10);
console.log('-----------------------------');
// NOT 논리 연산자 !
console.log(!true);
console.log(!false);
console.log(!5);
console.log(!'문장');
console.log(!!5);
console.log(!!'문장');
